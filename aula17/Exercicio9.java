package com.bruno.cursojava.aula17;

public class Exercicio9 {

	public static void main(String[] args) {
		/*faça um programa que imprima na tela apenas os números
		ípares entre 1 e 50.*/	
		int i=1;
		double impar=0;		
		for (i=1;i<50;i++) {			
		impar = i % 2;		
		if ( impar >0) {			
		System.out.println("O número "+i+" é ímpar.");			
}	
}
}
}


		
			
			



